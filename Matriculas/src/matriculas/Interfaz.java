    private void bRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            //
            Interfaz.cont += 1;

            String prg = pcombo.getSelectedItem().toString();
            String name = nombreText.getText();
            int num = 0;
            int strt = Integer.parseInt( ecombo.getSelectedItem().toString() );
            //
            if ( listaNombres.contains(name) || Interfaz.isNumeric(name) || name.isEmpty()) {
                
                javax.swing.JOptionPane.showMessageDialog(null, 
                        "Debe llenar todos los espacios con el tipo de dato "
                        + "indicado, puede haber indicado un nombre ya registrado",
                        "Error al ingresar los datos",JOptionPane.ERROR_MESSAGE);
                
            }else {
                
                switch (prg) {
                    case "Tecnica en Mercadeo":
                        programa ob1 = new programa(name, 1000, num, strt);
                        ob1.matricular();
                        listaNombres.add(ob1.getNombre());
                        if ( strt == 1 || strt == 2 || strt == 3 ){
                            ob1.setValor(1000 - 1000 * 0.232);
                            porcentajeText.setText("23.2%");
                        }else if (strt == 4 || strt == 5){
                            ob1.setValor(1000 - 1000 * 0.132);
                            porcentajeText.setText("13.2%");
                        }else {
                            porcentajeText.setText("No aplica");
                        }
                        costoText.setText( Integer.toString(1000) );
                        DineroText.setText(Double.toString( ob1.getValor() ));
                        //costoText.setText( Integer.toString( ob1.getNum() ) );
                        break;
                    case "Tecnica en Informatica":
                        programa ob2 = new programa(name, 1000, num, strt);
                        ob2.matricular();
                        listaNombres.add(ob2.getNombre());
                        if ( strt == 1 || strt == 2 || strt == 3 ){
                            ob2.setValor(1000 - 1000 * 0.232);
                            porcentajeText.setText("23.2%");
                        }else if (strt == 4 || strt == 5){
                            ob2.setValor(1000 - 1000 * 0.132);
                            porcentajeText.setText("13.2%");
                        }else {
                            porcentajeText.setText("No aplica");
                        }
                        costoText.setText( Integer.toString(1000) );
                        DineroText.setText(Double.toString( ob2.getValor() ));
                        break;
                    case "Tecnica en Contabilidad":
                        programa ob3 = new programa(name, 1200, num, strt);
                        ob3.matricular();
                        listaNombres.add(ob3.getNombre());
                        if ( strt == 1 || strt == 2 || strt == 3 ){
                            ob3.setValor(1200 - 1200 * 0.232);
                            porcentajeText.setText("23.2%");
                        }else if (strt == 4 || strt == 5){
                            ob3.setValor(1200 - 1200 * 0.132);
                            porcentajeText.setText("13.2%");
                        }else {
                            porcentajeText.setText("No aplica");
                        }
                        costoText.setText( Integer.toString(1200) );
                        DineroText.setText(Double.toString( ob3.getValor() ));
                        porcentajeText.setText("");
                        break;
                }
                javax.swing.JOptionPane.showMessageDialog(null, 
                        "El estudiante se ha matriculado con exito!",
                        "Registo satisfactorio",JOptionPane.INFORMATION_MESSAGE);
                
            }
        }catch (Exception ex){
                javax.swing.JOptionPane.showMessageDialog(null, 
                        "Debe llenar todos los espacios con el tipo de dato indicado",
                        "Error al ingresar los datos",JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_bRegistrarActionPerformed

