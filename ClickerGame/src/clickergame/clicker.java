    private void btnPresionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPresionarActionPerformed
        // TODO add your handling code here:
        x++;
        pbJugador4.setValue(x);
        
        if (x == 100)
        {
            if (pbJugador1.getValue() < 100 && pbJugador3.getValue() < 100 && pbJugador2.getValue() < 100)
            {
                double tiempo = Double.valueOf(pbJugador3.getValue());
                Jugadores j = new Jugadores(lblJugador.getText(),Double.toString(tiempo/5));
                Datos.add(j);
                try
                {
                    ObjectOutputStream escribirArchivo = new ObjectOutputStream(new FileOutputStream("records.car"));

                    escribirArchivo.writeObject(Datos);
                    escribirArchivo.close();

                }
                catch (Exception ex)
                {
                    JOptionPane.showMessageDialog(this, ex.getMessage(),"Error",JOptionPane.WARNING_MESSAGE);
                }
                JOptionPane.showMessageDialog(this, "Has ganado en " + Double.toString(tiempo/5) + " segundos" , "Ganador", JOptionPane.INFORMATION_MESSAGE); 
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Has perdido", "Perdedor", JOptionPane.INFORMATION_MESSAGE);
            }
            j1.Stop();
            j2.Stop();
            j3.Stop();
            x = 0;
            pbJugador1.setValue(x);
            pbJugador2.setValue(x);
            pbJugador3.setValue(x);
            pbJugador4.setValue(x);
            lblJugador.setText("Jugador 4");
            btnPresionar.setEnabled(false);
            btnCrear.setVisible(true);
        }
        
    }//GEN-LAST:event_btnPresionarActionPerformed

